package com.sml.referee.dto;

public class RefereeDto {
	private int refereeNumber;
	private String refereeName;
	private String refereeBirth;
	private String refereeAddress;
	private String refereePhoneNumber;
	private int refereeYes;
	private String refereePicture;
	private String refereeSport;
	
	public int getRefereeNumber() {
		return refereeNumber;
	}
	public void setRefereeNumber(int refereeNumber) {
		this.refereeNumber = refereeNumber;
	}
	public String getRefereeName() {
		return refereeName;
	}
	public void setRefereeName(String refereeName) {
		this.refereeName = refereeName;
	}
	public String getRefereeBirth() {
		return refereeBirth;
	}
	public void setRefereeBirth(String refereeBirth) {
		this.refereeBirth = refereeBirth;
	}
	public String getRefereeAddress() {
		return refereeAddress;
	}
	public void setRefereeAddress(String refereeAddress) {
		this.refereeAddress = refereeAddress;
	}
	public String getRefereePhoneNumber() {
		return refereePhoneNumber;
	}
	public void setRefereePhoneNumber(String refereePhoneNumber) {
		this.refereePhoneNumber = refereePhoneNumber;
	}
	public int getRefereeYes() {
		return refereeYes;
	}
	public void setRefereeYes(int refereeYes) {
		this.refereeYes = refereeYes;
	}
	public String getRefereePicture() {
		return refereePicture;
	}
	public void setRefereePicture(String refereePicture) {
		this.refereePicture = refereePicture;
	}
	public String getRefereeSport() {
		return refereeSport;
	}
	public void setRefereeSport(String refereeSport) {
		this.refereeSport = refereeSport;
	}
}
	
	